package org.usfirst.frc.team5437.robot;
/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	
	final public static int XBOX_CONTROLLER_ID = 0;
	
	final public static int FRONT_LEFT_WHEEL = 1;
	final public static int FRONT_RIGHT_WHEEL = 2;
	final public static int REAR_LEFT_WHEEL = 3;
	final public static int REAR_RIGHT_WHEEL = 4;
	
	final public static int FRONT_LEFT_SWIVEL = 15;
	final public static int FRONT_RIGHT_SWIVEL = 25;
	final public static int REAR_LEFT_SWIVEL = 35;
	final public static int REAR_RIGHT_SWIVEL = 45;
	
	final public static int FRONT_LEFT_ENCODER_CHANNEL_A = 0;
	final public static int FRONT_LEFT_ENCODER_CHANNEL_B = 1;
	final public static int FRONT_RIGHT_ENCODER_CHANNEL_A = 2;
	final public static int FRONT_RIGHT_ENCODER_CHANNEL_B = 3;
	final public static int REAR_LEFT_ENCODER_CHANNEL_A = 4;
	final public static int REAR_LEFT_ENCODER_CHANNEL_B = 5;
	final public static int REAR_RIGHT_ENCODER_CHANNEL_A = 6;
	final public static int REAR_RIGHT_ENCODER_CHANNEL_B = 7;
}
